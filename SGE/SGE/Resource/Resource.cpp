#include "Resource/Resource.h"

//====================================================================================================
// Class Definitions
//====================================================================================================

Resource::Resource()
	: mResourceID(0)
	, mRefCount(1)
{
	// Empty
}

//----------------------------------------------------------------------------------------------------

Resource::~Resource()
{
	// Empty
}

//----------------------------------------------------------------------------------------------------

Utility::Key Resource::GetResourceID() const
{
	return mResourceID;
}

//----------------------------------------------------------------------------------------------------

int Resource::GetRefCount() const
{
	return mRefCount;
}

//----------------------------------------------------------------------------------------------------

void Resource::SetResourceID(Utility::Key kID)
{
	mResourceID = kID;
}

//----------------------------------------------------------------------------------------------------

void Resource::IncrementRefCount()
{
	// Increment the reference count
	mRefCount++;
}

//----------------------------------------------------------------------------------------------------

void Resource::DecrementRefCount()
{
	// Decrement the reference count
	mRefCount--;
}